class NumberUtil:

    @staticmethod
    def additionoftwonumbers(num1, num2):
        return num1 + num2

    @staticmethod
    def subtractionoftwonumbers(num1, num2):
        return num1 - num2


if __name__ == '__main__':
    print(NumberUtil.additionoftwonumbers(10, 20))
    print(NumberUtil.subtractionoftwonumbers(10, 20))
